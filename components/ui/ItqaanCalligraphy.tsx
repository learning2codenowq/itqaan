export default function ItqaanCalligraphy({ style }: { style?: React.CSSProperties }) {
  const svgRef = useRef<SVGSVGElement>(null)

  useEffect(() => {
    const svg = svgRef.current
    if (!svg) return
    const paths = Array.from(svg.querySelectorAll<SVGPathElement>('path'))

    if (window.matchMedia('(prefers-reduced-motion: reduce)').matches) {
      gsap.set(paths, { fillOpacity: 1, strokeOpacity: 0 })
      return
    }

    const ctx = gsap.context(() => {
      paths.forEach(p => {
        const len = p.getTotalLength()
        gsap.set(p, { strokeDasharray: len, strokeDashoffset: len, fillOpacity: 0, strokeOpacity: 0.9 })
      })

      const tl = gsap.timeline()
      tl.to(paths, {
        strokeDashoffset: 0,
        duration: 0.55,
        ease: 'power1.inOut',
        stagger: 0.11,
      })
        .to(paths, { fillOpacity: 1, duration: 0.7, ease: 'power2.out', stagger: 0.05 }, 0.75)
        .to(paths, { strokeOpacity: 0, duration: 0.5, ease: 'power2.out' }, 1.3)
    }, svg)

    return () => ctx.revert()
  }, [])

  return (
    <svg
      ref={svgRef}
      viewBox={VIEW_BOX}
      xmlns="http://www.w3.org/2000/svg"
      aria-label="Itqaan, in Arabic calligraphy"
      role="img"
      style={{ display: 'block', overflow: 'visible', ...style }}
    >
      <g transform="scale(1,-1)" fill="var(--color-ember)" stroke="var(--color-ember)" strokeWidth="1.4" vectorEffect="non-scaling-stroke">
        {GLYPHS.map(([d, dx, dy], i) => (
          <path key={i} d={d} transform={`translate(${dx} ${dy})`} vectorEffect="non-scaling-stroke" />
        ))}
      </g>
    </svg>
  )
}
